    def ParseCommandLine(d):
        d["-a"] = False
        d["-l"] = False
        d["-s"] = False
        d["-q"] = False
        try:
            optlist, args = getopt.getopt(sys.argv[1:], "alstq")
        except getopt.GetoptError as e:
            msg, option = e
            print(msg)
            exit(1)
        for opt in optlist:
            if opt[0] == "-a":
                d["-a"] = True
            if opt[0] == "-l":
                d["-l"] = True
            if opt[0] == "-s":
                d["-s"] = True
            if opt[0] == "-t":
                exit(run(globals(), halt=True)[0])
            if opt[0] == "-q":
                d["-q"] = True
        if not d["-l"] and not args:
            Usage(d)
        return args

    def PrintItem(i, d):
        """Print the data; for some of the fields, print the number in
        additional units.
        """
        print(i[0])
        if not d["-q"]:
            for name, item in zip(field_names[1:], i[1:]):
                print(fmt % (maxlen, name, item), end="")
                if name == "MW_g/mol":
                    mw = float(item)
                elif name == "Tc_K":
                    T0, s, T_K = 273.15, [], float(item)
                    n = GetSigFig(item)
                    s += [sig(T_K - T0, n) + " C"]
                    s += [sig(9 / 5.0 * (T_K - T0) + 32, n) + " F"]
                    print(" (", ", ".join(s), ")", sep="", end="")
                elif name == "Pc_MPa":
                    P_Pa, s = float(item) * 1e6, []
                    n = GetSigFig(item)
                    s += [sig(to(P_Pa, "bar"), n) + " bar"]
                    s += [sig(to(P_Pa, "atm"), n) + " atm"]
                    s += [sig(to(P_Pa, "psi"), n) + " psi"]
                    print(" (", ", ".join(s), ")", sep="", end="")
                elif name == "Vc_m3/kmol":
                    # Also print density in g/cc
                    x = sig(float(item) / mw / 1000)
                    print(" (%s g/cc)" % sig(x, GetSigFig(item)), end="")
                print()
            print()

