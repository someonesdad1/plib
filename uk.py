    def B_to_US():
        '''Return a dict to convert British spellings to US spellings.  Note all the words are
        lowercase.
        '''
        if g.b2us is None:
            di = {}
            for line in g.data.split("\n"):
                line = line.strip()
                if not line:
                    continue
                us, br = [i.lower() for i in line.split()]
                if br in di:
                    Error(f"{br!r} already in dict: {di[br]!r}")
                di[br] = us
            g.b2us = di
        return g.b2us
    def US_to_B():
        '''Return a dict to convert US spellings to British spellings.  Note all the words are
        lowercase.
        '''
        if g.us2b is None:
            di = {}
            for line in g.data.split("\n"):
                line = line.strip()
                if not line:
                    continue
                us, br = [i.lower() for i in line.split()]
                if us in di:
                    Error(f"{us!r} already in dict: {di[us]!r}")
                di[us] = br
            g.us2b = di
        return g.us2b
    def ProcessFile(file, reverse=False):
        if file == "-":
            s = sys.stdin.read()
        else:
            p = P(file)
            s = p.open().read()
        dq = get.Tokenize(s)
        # Only keep the get.wrd types
        words_in_file = [i for i in dq if ii(i, get.wrd)]
        # Convert to all lowercase
        words_in_file = set(i.lower() for i in words_in_file)
        # Scan for words
        if reverse:
            # US to UK
            di, out = US_to_B(), []
            for word in words_in_file:
                if word in di:
                    out.append((word, di[word]))
        else:
            # Uk to US
            di, out = B_to_US(), []
            for word in words_in_file:
                if word in di:
                    out.append((word, di[word]))
        # Report
        if out:
            s = "<stdin>" if file == "-" else file
            print(f"{'<stdin>' if file == '-' else file}")
            w = max(len(i[0]) for i in out)
            if reverse:
                for us, uk in sorted(out):
                    print(f"{us:{w}s}  -->  {uk}")
            else:
                for uk, us in sorted(out):
                    print(f"{uk:{w}s}  -->  {us}")

