class Density(float):
    '''Contain a density entry.  It's a floating point number
    representing the density in kg/m3 of a material.

    Attributes:
        range       None if it's a single value or (low, high) to represent
                    the given range where low and high are floats.
        category    Integer representing the material category (see the
                    dictionary categories)
        ref         Reference that attributes the value.  This will be
                    a 3-letter string with optional page number.
    '''
    maxlen = 50     # Maximum formatting length of name string
    def __new__(cls, name, rho, category, reference):
        '''
        name        String describing the material
        rho         String for a density in kg/m3 or a range (two
                    numbers separated by a hyphen)
        category    Integer for category (metal, mineral, etc.).  0
                    means no category is assigned.
        reference   3-letter string attributing the source and optional
                    page number. 
        '''
        mean_density, rng = Density.GetRho(rho)
        instance = super().__new__(cls, mean_density)
        instance.rho = rho
        instance.name = name
        instance.range = rng
        instance.category = category
        instance.reference = Density.CheckReference(reference)
        instance._check()
        return instance
    def _check(self):
        'Check consistency'
        assert(ii(self.name, str) and self.name)
        if ii(self.range, tuple):
            assert(ii(i, float) for i in self.range)
        else:
            assert(self.range is None)
        assert(ii(self.category, int) and self.category >= 0)
        assert(ii(self.reference, str))
    def __str__(self):
        'Print in form suitable for a sequence'
        s = f"{self.name!r},"
        r = f"'{self.rho!s}',"
        c = f"{self.category},"
        return f"Density({s:{Density.maxlen}s} {r:12s} {c:5s} {self.reference!r}),"
    def __lt__(self, other):
        'Allow sorting by the density value'
        assert(ii(other, Density))
        return self.get_rho() < other.get_rho()
    @property
    def ref3(self):
        'Returns 3-letter reference string'
        if " " in self.reference:
            return self.reference.split()[0]
        return self.reference
    @classmethod
    def CheckReference(cls, s: str):
        'Validate a reference string'
        try:
            if " " in s:
                t, pg = s.split()
                references[t]
            else:
                references[s]
            return s
        except Exception:
            raise ValueError(f"'{s}' is not a valid reference string")
    @classmethod
    def GetRho(cls, s: str):
        '''s is a density string in kg/m3 or a range of such densities.  Return
        (nominal_value: float, low_high) where low_high is either None or a
        tuple of floats giving the density's range.
        '''
        digits = 5 
        if "-" in s:
            x = tuple(RoundOff(float(i), digits=digits) for i in s.split("-"))
            mean = sum(x)/2
            return (mean, x)
        else:
            x = RoundOff(float(s), digits=digits)
            return (x, None)
